export function estimateCalories(caloriesPerMin, durationMin, profile = userProfile) {
  const { weight = 70, height = 170, age = 30, sex = 'male' } = profile;
  const bmr =
    sex === 'female'
      ? 447.593 + 9.247 * weight + 3.098 * height - 4.33 * age
      : 88.362 + 13.397 * weight + 4.799 * height - 5.677 * age;
  const metScaler = weight / 70;
  return Math.round(caloriesPerMin * durationMin * metScaler);
}

